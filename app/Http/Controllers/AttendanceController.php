    }

    public function memberslive(){
        $att = Member::select("*")
            ->join("attendances", "attendances.member_id", "=", "members.id")
            ->where("attendances.status",1)
            ->orderBy('attendances.id', 'DESC')->get();

            return response()->json($att);
    }

